#include	<unistd.h>
#include	<stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	cadena[] = "perfecto";
	char	*str;
	int		longitud;

	str = cadena;
	longitud = ft_strlen(str);
	printf("\nlongitud de '%s' = %d\n", cadena, longitud);
	return (0);
}
