#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen(s);
	while (s[len] != (unsigned char)c && len > 0)
		len--;
	if (s[len] == (unsigned char)c)
		return ((char *)&s[len]);
	return (NULL);
}
