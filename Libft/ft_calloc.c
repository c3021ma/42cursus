#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*mem;

	mem = malloc((int)count * size);
	if (mem == NULL)
		return (NULL);
	ft_bzero(mem, count * size);
	return ((void *)mem);
}
