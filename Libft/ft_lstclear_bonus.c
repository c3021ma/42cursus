#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;
	t_list	*test;

	test = *lst;
	while (test)
	{
		tmp = test -> next;
		ft_lstdelone(test, del);
		test = tmp;
	}
	*lst = NULL;
}
