#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*fts;

	i = 0;
	fts = (unsigned char *)s;
	while (i < n)
	{
		if (*fts == (unsigned char)c)
			return (fts);
		i++;
		fts++;
	}
	return (NULL);
}
