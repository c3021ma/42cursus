#include	<stdio.h>
#include	<ctype.h>
#include	"libft.h"

int	ft_isdigit(int c)
{
	int				n;

	n = 1;
	if (c >= 48 && c <= 57)
	{
		n = 1;
	}
	else
	{
		n = 0;
	}
	return (n);
}
